"""Plotting utilities for PITMonitor additional experiments.

Reads `results.json` generated by `run_additional.py` and writes figures to
`out/figures` by default.

Usage
-----
python plot_additional.py
python plot_additional.py --input out/results.json
python plot_additional.py --output-dir out/figures
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np

_THIS_DIR = Path(__file__).resolve().parent


def _apply_style() -> None:
    plt.rcParams.update(
        {
            "font.family": "serif",
            "font.serif": ["CMU Serif", "Computer Modern Roman", "DejaVu Serif"],
            "mathtext.fontset": "cm",
            "axes.unicode_minus": False,
            "font.size": 9,
            "axes.titlesize": 11,
            "axes.titleweight": "bold",
            "axes.labelsize": 10,
            "xtick.labelsize": 9,
            "ytick.labelsize": 9,
            "legend.fontsize": 9,
            "figure.dpi": 150,
            "savefig.dpi": 300,
            "savefig.bbox": "tight",
            "axes.spines.top": False,
            "axes.spines.right": False,
            "axes.grid": True,
            "grid.alpha": 0.25,
            "grid.linestyle": ":",
        }
    )


def _save(fig: plt.Figure, path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(path)
    plt.close(fig)
    print(f"Saved {path}")


def plot_proposition_fit(results: dict, out_dir: Path) -> None:
    """Plot formula vs empirical E[e_t|A] curves for aligned/misaligned priors."""
    cases = results["proposition_verification"]["cases"]
    fig, axes = plt.subplots(1, 2, figsize=(11.5, 4.2), sharey=True)

    for ax, mode in zip(axes, ["aligned", "misaligned"]):
        rows = sorted(cases[mode]["rows"], key=lambda r: r["n_post_before_t"])
        n = np.array([r["n_post_before_t"] for r in rows], dtype=float)
        formula = np.array([r["formula_mean_e"] for r in rows], dtype=float)
        emp = np.array([r["empirical_mean_e"] for r in rows], dtype=float)
        se = np.array([r["empirical_se"] for r in rows], dtype=float)

        asymptotic = float(cases[mode]["asymptotic_target_Bsumtheta2"])
        gamma = float(cases[mode]["gamma"])
        n_star = float(cases[mode]["n_star_formula"])

        ax.plot(n, formula, color="#1f77b4", lw=1.8, label="Formula")
        ax.plot(n, emp, "o-", color="#d62728", markersize=4, lw=1.0, label="Empirical")
        ax.fill_between(
            n,
            emp - 1.96 * se,
            emp + 1.96 * se,
            color="#d62728",
            alpha=0.15,
            linewidth=0,
            label="Empirical 95% CI",
        )
        ax.axhline(1.0, color="0.35", lw=1.1, ls="--", label="Null baseline (1)")
        ax.axhline(
            asymptotic, color="#2ca02c", lw=1.2, ls="-.", label="Asymptotic target"
        )

        if gamma < 0 and n_star > 0:
            ax.axvline(n_star, color="#9467bd", lw=1.1, ls=":", label=r"Warmup $n^*$")

        ax.set_xscale("log")
        ax.set_title(f"{mode.capitalize()} prior")
        ax.set_xlabel("Post-shift lag n (samples)")

    axes[0].set_ylabel(r"$\mathbb{E}[e_t\mid A]$")
    handles, labels = axes[1].get_legend_handles_labels()
    by_label = dict(zip(labels, handles))
    axes[1].legend(by_label.values(), by_label.keys(), loc="best")

    fig.suptitle("Finite-Time Mean Formula Verification", y=1.02)
    _save(fig, out_dir / "fig_proposition_formula_fit.png")


def _format_regime_name(name: str) -> str:
    mapping = {
        "iid_uniform_baseline": "IID Uniform",
        "stationary_dependent_ar1_uniform_margin": "Dependent\nAR(1)",
        "mild_nonstationary_symmetric_beta_trend": "Mild\nNonstationary",
        "seasonal_symmetric_beta": "Seasonal",
    }
    return mapping.get(name, name)


def plot_alarm_rates(results: dict, out_dir: Path) -> None:
    """Plot stream-level alarm rates with 95% Wilson confidence intervals."""
    rows = results["stream_regime_verification"]
    alpha = float(results["config"]["alpha"])

    labels = [_format_regime_name(r["name"]) for r in rows]
    rates = np.array([r["alarm_rate"] for r in rows], dtype=float)
    ci = np.array([r["alarm_rate_ci95"] for r in rows], dtype=float)
    yerr = np.vstack([rates - ci[:, 0], ci[:, 1] - rates])

    fig, ax = plt.subplots(figsize=(8.2, 4.2))
    x = np.arange(len(labels))

    colors = ["#4c78a8", "#f58518", "#54a24b", "#b279a2"]
    ax.bar(x, rates, color=colors, alpha=0.85, width=0.72)
    ax.errorbar(x, rates, yerr=yerr, fmt="none", ecolor="0.2", capsize=4, lw=1.0)
    ax.axhline(
        alpha, color="#d62728", ls="--", lw=1.3, label=rf"Target $\alpha={alpha:.2f}$"
    )

    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Alarm rate (ever alarm)")
    ax.set_ylim(0.0, max(0.15, np.max(ci[:, 1]) * 1.25))
    ax.set_title("Alarm Rates Across Verification Regimes")
    ax.legend(loc="upper left")

    _save(fig, out_dir / "fig_regime_alarm_rates.png")


def plot_regime_diagnostics(results: dict, out_dir: Path) -> None:
    """Plot marginal/dependence diagnostics for each regime."""
    rows = results["stream_regime_verification"]
    labels = [_format_regime_name(r["name"]) for r in rows]

    metrics = [
        ("mean", "Mean of PIT", 0.5),
        ("var", "Variance of PIT", 1.0 / 12.0),
        ("lag1_autocorr", "Lag-1 autocorrelation", 0.0),
        ("ks_uniform", "KS statistic vs U[0,1]", 0.0),
    ]

    fig, axes = plt.subplots(2, 2, figsize=(10.0, 6.0))
    axes = axes.ravel()

    x = np.arange(len(labels))
    colors = ["#4c78a8", "#f58518", "#54a24b", "#b279a2"]

    for ax, (key, title, ref) in zip(axes, metrics):
        vals = np.array([r["diagnostics"][key] for r in rows], dtype=float)
        ax.bar(x, vals, color=colors, alpha=0.85, width=0.72)
        ax.axhline(ref, color="#d62728", ls="--", lw=1.0)
        ax.set_title(title)
        ax.set_xticks(x)
        ax.set_xticklabels(labels)

    fig.suptitle("PIT Stream Diagnostics by Regime", y=1.02)
    fig.tight_layout()
    _save(fig, out_dir / "fig_regime_diagnostics.png")


def plot_multistep_localization(results: dict, out_dir: Path) -> None:
    """Plot localization errors to onset and max-intensity phase."""
    loc = results.get("multistep_localization_verification")
    if not loc:
        return

    mae_onset = float(loc["mae_to_onset"])
    mae_max = float(loc["mae_to_max_phase"])
    signed_onset = np.asarray(loc.get("signed_error_to_onset", []), dtype=float)
    signed_max = np.asarray(loc.get("signed_error_to_max_phase", []), dtype=float)

    fig, axes = plt.subplots(1, 2, figsize=(10.5, 4.2))

    axes[0].bar(
        ["Onset", "Max phase"],
        [mae_onset, mae_max],
        color=["#4c78a8", "#f58518"],
        alpha=0.88,
        width=0.65,
    )
    axes[0].set_ylabel("Mean absolute localization error (samples)")
    axes[0].set_title("Localization MAE Targets")

    data = [signed_onset, signed_max]
    axes[1].boxplot(
        data,
        vert=False,
        tick_labels=["Error to onset", "Error to max phase"],
        widths=0.65,
        patch_artist=True,
        boxprops={"facecolor": "#9ecae1", "alpha": 0.8},
        medianprops={"color": "#d62728", "linewidth": 1.2},
    )
    axes[1].axvline(0.0, color="0.25", ls="--", lw=1.0)
    axes[1].set_xlabel("Signed localization error (estimate - reference)")
    axes[1].set_title("Signed Error Distribution")

    n_cp = int(loc.get("n_with_changepoint_estimate", 0))
    fig.suptitle(f"Multi-Step Drift Localization (n cp-estimates = {n_cp})", y=1.03)
    fig.tight_layout()
    _save(fig, out_dir / "fig_multistep_localization.png")


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Plot PITMonitor additional experiment results"
    )
    p.add_argument("--input", type=str, default="out/results.json")
    p.add_argument("--output-dir", type=str, default="out/figures")
    return p.parse_args()


def main() -> None:
    _apply_style()
    args = parse_args()

    in_path = (
        (_THIS_DIR / args.input).resolve()
        if not Path(args.input).is_absolute()
        else Path(args.input)
    )
    out_dir = (
        (_THIS_DIR / args.output_dir).resolve()
        if not Path(args.output_dir).is_absolute()
        else Path(args.output_dir)
    )

    with in_path.open("r", encoding="utf-8") as f:
        results = json.load(f)

    plot_proposition_fit(results, out_dir)
    plot_alarm_rates(results, out_dir)
    plot_regime_diagnostics(results, out_dir)
    plot_multistep_localization(results, out_dir)


if __name__ == "__main__":
    main()
