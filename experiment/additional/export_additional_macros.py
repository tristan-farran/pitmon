"""Export LaTeX macros from additional experiment JSON results.

Usage
-----
python export_additional_macros.py
python export_additional_macros.py --input out/results.json --output out/additional_macros.tex
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

_THIS_DIR = Path(__file__).resolve().parent


def pct(x: float) -> str:
    return f"{100.0 * x:.1f}\\%"


def f3(x: float) -> str:
    return f"{x:.3f}"


def f1(x: float) -> str:
    return f"{x:.1f}"


def as_int(x: float | int) -> str:
    # Use {,} so the same macro is safe in both text and math mode.
    return f"{int(x):,}".replace(",", "{,}")


def emit(name: str, value: str) -> str:
    return f"\\newcommand{{\\{name}}}{{{value}}}"


def build_macros(data: dict) -> str:
    lines: list[str] = []
    lines.append(
        "% Auto-generated by export_additional_macros.py - do not edit manually."
    )

    cfg = data["config"]
    lines.append(emit("addAlpha", str(cfg["alpha"])))
    lines.append(emit("addBins", as_int(cfg["n_bins"])))
    lines.append(emit("addNTrialsStream", as_int(cfg["n_trials_stream"])))
    lines.append(emit("addNStepsStream", as_int(cfg["n_steps_stream"])))

    summary = data["summary"]
    lines.append(
        emit(
            "addFiniteErrAligned",
            f3(summary["proposition_formula_accuracy"]["aligned"]),
        )
    )
    lines.append(
        emit(
            "addFiniteErrMisaligned",
            f3(summary["proposition_formula_accuracy"]["misaligned"]),
        )
    )

    stream = {r["name"]: r for r in data["stream_regime_verification"]}

    def add_stream(prefix: str, key: str) -> None:
        row = stream[key]
        lines.append(emit(f"{prefix}AlarmRate", pct(row["alarm_rate"])))
        ci_lo, ci_hi = row["alarm_rate_ci95"]
        lines.append(emit(f"{prefix}AlarmCILo", pct(ci_lo)))
        lines.append(emit(f"{prefix}AlarmCIHi", pct(ci_hi)))
        lines.append(
            emit(
                f"{prefix}MedianAlarmT",
                as_int(round(row["median_alarm_time_conditional"])),
            )
        )

    add_stream("addIid", "iid_uniform_baseline")
    add_stream("addDep", "stationary_dependent_ar1_uniform_margin")
    add_stream("addMild", "mild_nonstationary_symmetric_beta_trend")
    add_stream("addSeas", "seasonal_symmetric_beta")

    loc = data["multistep_localization_verification"]
    lines.append(emit("addLocAlarmRate", pct(loc["alarm_rate"])))
    lines.append(emit("addLocMAEOnset", f1(loc["mae_to_onset"])))
    lines.append(emit("addLocMAEMax", f1(loc["mae_to_max_phase"])))
    lines.append(
        emit("addLocMedSignedOnset", as_int(round(loc["median_signed_error_to_onset"])))
    )
    lines.append(
        emit(
            "addLocMedSignedMax", as_int(round(loc["median_signed_error_to_max_phase"]))
        )
    )
    lines.append(
        emit("addLocCloserOnset", as_int(loc["closer_reference_counts"]["onset"]))
    )
    lines.append(
        emit("addLocCloserMax", as_int(loc["closer_reference_counts"]["max_phase"]))
    )
    lines.append(emit("addLocN", as_int(loc["n_with_changepoint_estimate"])))

    return "\n".join(lines) + "\n"


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Export additional experiment LaTeX macros")
    p.add_argument("--input", type=str, default="out/results.json")
    p.add_argument("--output", type=str, default="out/additional_macros.tex")
    return p.parse_args()


def main() -> None:
    args = parse_args()
    in_path = (
        (_THIS_DIR / args.input).resolve()
        if not Path(args.input).is_absolute()
        else Path(args.input)
    )
    out_path = (
        (_THIS_DIR / args.output).resolve()
        if not Path(args.output).is_absolute()
        else Path(args.output)
    )

    with in_path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(build_macros(data), encoding="utf-8")
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
